export const getPriceFromTable = (table, projection, width) => {
  const roundedProjection = roundUpToNextAvailableProjection(table, projection);
  const roundedWidth = roundUpToNextAvailableWidth(table, projection, width);
  
  if (table[roundedProjection] && table[roundedProjection][roundedWidth] !== undefined) {
    return table[roundedProjection][roundedWidth];
  }
  
  return 0;
};

